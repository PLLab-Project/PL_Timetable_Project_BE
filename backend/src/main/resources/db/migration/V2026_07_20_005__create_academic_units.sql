COMMENT ON TABLE academic_colleges IS
    'Canonical college codes derived from versioned academic curriculum datasets.';
COMMENT ON TABLE academic_units IS
    'Canonical department, major, and program codes used across catalog and requirement domains.';
COMMENT ON COLUMN academic_units.code_source IS
    'OFFICIAL_CURRICULUM for supplied department codes; REQUIREMENT_DERIVED for deterministic keys without an official code.';
COMMENT ON COLUMN academic_units.is_current IS
    'True when the unit appears in the latest loaded curriculum dataset; not an admissions-status claim.';
COMMENT ON TABLE academic_unit_aliases IS
    'Year-scoped names used to resolve curriculum and graduation-requirement records to canonical units.';
COMMENT ON TABLE section_academic_units IS
    'Many-to-many mapping between catalog sections and explicitly supplied academic-unit contexts.';
